import { readFileSync } from 'fs';

// All station IDs from vedur.is forecast areas relevant to eclipse path
const areas = {
  reykjavik: [1, 1471, 1473, 1474, 1477, 1479, 1481, 1482, 31109, 31488, 31579, 1578],
  faxafloi: [990, 1361, 1453, 1487, 1590, 1596, 1777, 1868, 1919, 1936, 6300, 6802, 7001, 31380, 31392, 31572, 31932, 31948, 31985, 32097, 36504],
  breidafjordur: [178, 1924, 1938, 2175, 2266, 2304, 2319, 2481, 31932, 31985, 32097, 32250, 32336, 32355, 32390],
  westfjords: [293, 2315, 2319, 2428, 2481, 2530, 2631, 2644, 2655, 2738, 2862, 2941, 32336, 32355, 32390],
};

// Deduplicate all IDs
const allIds = [...new Set(Object.values(areas).flat())].sort((a, b) => a - b);
console.error(`Fetching ${allIds.length} unique station IDs...`);

const url = `https://xmlweather.vedur.is/?op_w=xml&type=forec&lang=en&view=xml&ids=${allIds.join(';')}`;
const resp = await fetch(url, { headers: { 'User-Agent': 'EclipseChase/1.0' } });
const xml = await resp.text();

const re = /<station id="(\d+)"[^>]*>\s*<name>([^<]*)<\/name>[\s\S]*?<lat>([^<]*)<\/lat>\s*<lon>([^<]*)<\/lon>/g;
const seen = new Set();
const stations = [];

let m;
while ((m = re.exec(xml)) !== null) {
  if (!seen.has(m[1])) {
    seen.add(m[1]);
    stations.push({ id: m[1], name: m[2], lat: m[3], lng: m[4] });
  }
}

// Determine region for each station
function getRegion(id) {
  const n = parseInt(id);
  for (const [region, ids] of Object.entries(areas)) {
    if (ids.includes(n)) return region;
  }
  return 'unknown';
}

// Map vedur.is area names to our region names
const regionMap = {
  reykjavik: 'reykjavik',
  faxafloi: 'reykjanes',  // Faxaflói covers Reykjanes + Borgarfjörður
  breidafjordur: 'snaefellsnes',
  westfjords: 'westfjords',
};

// Better region assignment based on coordinates
function getRegionByCoords(lat, lng, vedurRegion) {
  const la = parseFloat(lat);
  const lo = parseFloat(lng);

  // Westfjords: north of 65.3 and west of -21.5
  if (la > 65.3 && lo < -21.5) return 'westfjords';
  // Snæfellsnes: lat 64.7-65.3, lng west of -22
  if (la >= 64.7 && la <= 65.3 && lo < -22) return 'snaefellsnes';
  // Borgarfjörður: lat 64.4-65.0, lng -21 to -22
  if (la >= 64.4 && la <= 65.1 && lo >= -22 && lo < -20.5) return 'borgarfjordur';
  // Reykjanes: lat below 64.2, lng west of -21
  if (la < 64.2 && lo < -21) return 'reykjanes';
  // Reykjavík area
  if (la >= 64.0 && la <= 64.3 && lo >= -22.1 && lo < -21) return 'reykjavik';

  return regionMap[vedurRegion] || 'reykjavik';
}

console.log('-- Weather stations from vedur.is forecast areas');
console.log('-- Generated by scripts/fetch-stations.mjs');
console.log('');

for (const s of stations.sort((a, b) => parseInt(a.id) - parseInt(b.id))) {
  const vedurRegion = getRegion(s.id);
  const region = getRegionByCoords(s.lat, s.lng, vedurRegion);
  console.log(`${s.id}|${s.name}|${s.lat}|${s.lng}|${region}`);
}

console.error(`\nFound ${stations.length} stations with coordinates out of ${allIds.length} requested.`);
